"""
Python = Linguagem de programação
Tipo de tipagem = Dinamica / Forte
str -> strings -> texto
Strings são textos que estão dentro de aspas

"""
print(1234)#numero inteiro
# Aspas simples
print('sivaldo vieira')#string ('')


# Aspas duplas   
print("Sivaldo vieira")#string ("")
print(1 ,'Sivaldo "vieira" ')#string com escape ("")



# Escape
print("Sivaldo \"vieira\"")#string com escape coloca aspas na palavra ("")
print('Sivaldo "vieira"')#string com escape ("")

# r
print(r"Sivaldo \"vieira\"")#string com escape coloca (\) na palavra .
