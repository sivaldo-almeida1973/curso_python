# Tipos (Int) e (Float)
# int -> Números inteiros
# O tipo int representa qualquer nùmero
# Positivo ou negativo. int sem sinal é considerado.
print(11) #int#
print(-11) #int
print(0) #int

# Float -> Nùmero com ponto flutuante
# o tipo Float representa qualquer número
# positivo ou Negativo com ponto flutuante.
# float sem sinal é considerado positivo.

print(1.1, 10.11) #float
print(0.0) #float
print(0.0, -1.2) #float

# A classe type mostra o tipo que o Python inferiu ao valor.

print(  type('sivaldo')   )
print(  type(3)   )
print(  type(1.1) ,type(-1.2)  )